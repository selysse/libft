#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*cpp;

	i = 0;
	cpp = (char*)s;
	while (n > 0)
	{
		cpp[i] = '\0';
		i++;
		n--;
	}
}
